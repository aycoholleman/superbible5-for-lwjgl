package superbible5.lwjgl;

@SuppressWarnings("serial")
public class OpenGLException extends RuntimeException {

	public OpenGLException()
	{
	}


	public OpenGLException(String arg0)
	{
		super(arg0);
	}


	public OpenGLException(Throwable arg0)
	{
		super(arg0);
	}


	public OpenGLException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}


	public OpenGLException(String arg0, Throwable arg1, boolean arg2, boolean arg3)
	{
		super(arg0, arg1, arg2, arg3);
	}

}
