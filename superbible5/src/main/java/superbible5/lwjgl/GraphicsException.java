package superbible5.lwjgl;

@SuppressWarnings("serial")
public class GraphicsException extends RuntimeException {

	public GraphicsException()
	{
	}


	public GraphicsException(String arg0)
	{
		super(arg0);
	}


	public GraphicsException(Throwable arg0)
	{
		super(arg0);
	}


	public GraphicsException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}


	public GraphicsException(String arg0, Throwable arg1, boolean arg2, boolean arg3)
	{
		super(arg0, arg1, arg2, arg3);
	}

}
